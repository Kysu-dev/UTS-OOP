public class Penduduk {
    private int NIK;
    private String nama;

    public Penduduk(int NIK, String nama) {
        this.NIK = NIK;
        this.nama = nama;
    }

    public int getNIK() {
        return NIK;
    }

    public String getNama() {
        return nama;
    }

    public void setNIK(int NIK) {
        this.NIK = NIK;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }
    
    
}
